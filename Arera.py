A=input()
B=input()
C=input()
A=float(A)
B=float(B)
C=float(C)
pi=3.14159
Triangulo=(A*C)/2
Circulo=pi*(C*C)
Trapezio=((A+B)*C)/2
Quadrado=B*B
Retangulo=A*B
print(f'TRINGULO : {Triangulo:0.3f}')
print(f'CIRCULO : {Circulo:0.3f}')
print(f'TRAPEZIO : {Trapezio:0.3f}')
print(f'QUADRADO : {Quadrado:0.3f}')
print(f'RETANGULO : {Retangulo:0.3f}')